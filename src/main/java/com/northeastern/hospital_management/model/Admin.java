package com.northeastern.hospital_management.model;

/**
 *
 * @author imkus
 */
public class Admin {

    String email;
    String password;

    public enum Role {
        SYSTEM_ADMIN, HOSPITAL_ADMIN, COMMUNITY_ADMIN;
    }
    Role role;

    public Admin(String email, String password, Role role) {
        this.email = email;
        this.password = password;
        this.role = role;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

}
